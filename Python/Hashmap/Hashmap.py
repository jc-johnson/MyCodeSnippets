streetno = {"1":"Sachine Tendulkar", "2":"Dravid", "3":"Sehwag", "4":"Laxman","5":"Kohli"}

# as well as using the dict keyword:
streetno = dict({"1":"Sachine Tendulkar", "2":"Dravid"})

#or
streetno = {}
streetno["1"] = "Sachine Tendulkar" 